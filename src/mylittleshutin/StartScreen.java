    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
		try {
			dataOps = new DatabaseOps();
			dataOps.estabConnection();
			dataOps.createDatabase();
			dataOps.insertSamples();
		} catch (Exception err) {
			System.err.println("Cannot create Database: " + err);
		}
		
		if(populateTable() == false) {
			dataOps.insertSamples();
			populateTable();
		}
    }//GEN-LAST:event_formWindowOpened

    private void loadGameBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loadGameBtnMouseClicked
        //selectedSave = selectedSave.listToCharacter(dataOps.getRecord("TESTTESTTEST"));
		if(selectedSave == null) {
			JOptionPane.showMessageDialog(this, "No Save Selected!");
		} else {
			GameInstance GI = new GameInstance();
			GI.getCharacter(selectedSave);
			dataOps.closeConnection();
			GI.setVisible(true);
			this.setVisible(false);
		}

    }//GEN-LAST:event_loadGameBtnMouseClicked

    private void saveTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveTableMouseClicked
		int i = saveTable.getSelectedRow();	//Gets name from Selected Row, searches for record.
		String selectedName = (String)saveTable.getValueAt(i, 0);
		System.out.println("SAVED NAME: " + selectedName);
		selectedSave = selectedSave.listToCharacter(dataOps.getRecord(selectedName));
		System.out.println(selectedSave);
    }//GEN-LAST:event_saveTableMouseClicked

