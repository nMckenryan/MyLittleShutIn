	private boolean populateTable() { //Populates Save table at StartScreen
		try {
			dataOps = new DatabaseOps();
			dataOps.estabConnection();
			ArrayList recordList = dataOps.getRecord("ÃŸ");
			DefaultTableModel model = (DefaultTableModel) saveTable.getModel();
			for(Object si : recordList) {
				ShutIn record = (ShutIn)si;
				model.addRow(new Object[] {record.getShutInName(), record.getTextBodyweight(), record.getDaysPassed()});
			}
			return true;
		} catch(Exception err) {
			System.err.println(err);
			return false;
		}
	}
	
